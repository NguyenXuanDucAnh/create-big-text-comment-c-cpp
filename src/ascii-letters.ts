/**
 * Convert a string to ASCII art
 * @param text - The text to convert (only letters A-Z supported)
 * @param spacing - Number of dots between characters (default: 2)
 * @returns Array of 7 strings representing the ASCII art
 */
export function textToAscii(text: string, spacing: number = 2): string[] {
  const lines: string[] = ['', '', '', '', '', '', ''];
  const upperText = text.toUpperCase();
  const spacer = '.'.repeat(spacing);

  for (let i = 0; i < upperText.length; i++) {
    const char = upperText[i];
    
    if (char === ' ') {
      // Add space between words
      for (let line = 0; line < 7; line++) {
        lines[line] += '.....';
      }
    } else if (ASCII_LETTERS[char]) {
      const charLines = ASCII_LETTERS[char];
      for (let line = 0; line < 7; line++) {
        lines[line] += charLines[line];
        if (i < upperText.length - 1 && upperText[i + 1] !== ' ') {
          lines[line] += spacer;
        }
      }
    }
  }

  return lines;
}

// /**
//  * Generate a C/C++ style comment with ASCII art
//  * @param text - The text to convert
//  * @param commentStyle - 'block' for /* */ or 'line' for //
//  * @returns The formatted comment string
//  */
export function generateAsciiComment(
  text: string, 
  commentStyle: 'block' | 'line' = 'block'
): string {
  const asciiLines = textToAscii(text);
  
  if (commentStyle === 'block') {
    return '/*\n' + asciiLines.map(line => ' * ' + line).join('\n') + '\n */';
  } else {
    return asciiLines.map(line => '// ' + line).join('\n');
  }
}